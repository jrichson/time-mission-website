/**
 * Emits src/data/site/astro-rendered-output-files.json from Astro page modules.
 * Humans maintain routes.json and src/pages; this manifest is machine-derived for sync + checks.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const pagesDir = path.join(root, 'src', 'pages');
const routesPath = path.join(root, 'src', 'data', 'routes.json');
const outPath = path.join(root, 'src', 'data', 'site', 'astro-rendered-output-files.json');

function walkAstroFiles(dir, base = '') {
  const out = [];
  if (!fs.existsSync(dir)) return out;
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const rel = path.join(base, ent.name).replace(/\\/g, '/');
    const full = path.join(dir, ent.name);
    if (ent.isDirectory()) {
      out.push(...walkAstroFiles(full, rel));
    } else if (ent.name.endsWith('.astro')) {
      out.push(rel);
    }
  }
  return out;
}

function astroRelToOutputHtml(rel) {
  const parts = rel.split('/');
  const file = parts.pop();
  const stem = file.replace(/\.astro$/, '');
  const segs = [...parts, stem === 'index' ? 'index.html' : `${stem}.html`].filter(Boolean);
  return segs.join('/');
}

/** e.g. c/[slug].astro — emits paths at build time, not registrable as a single routes.json row */
function hasDynamicSegment(relPath) {
  return relPath.split('/').some((segment) => segment.includes('[') && segment.includes(']'));
}

const astroRels = walkAstroFiles(pagesDir);
const dynamicLandingModules = astroRels.filter(hasDynamicSegment).sort();
const staticAstroRels = astroRels.filter((rel) => !hasDynamicSegment(rel));

const outputFiles = [...new Set(staticAstroRels.map(astroRelToOutputHtml))].sort();

const registry = JSON.parse(fs.readFileSync(routesPath, 'utf8'));
const routeOutputs = new Set(
  (registry.routes || []).map((r) => String(r.outputFile || '').replace(/^\//, '')),
);

const errors = [];
for (const out of outputFiles) {
  if (!routeOutputs.has(out)) {
    errors.push(`Compiled Astro output "${out}" has no matching routes.json outputFile`);
  }
}

if (errors.length) {
  console.error('compile-route-artifacts failed:');
  for (const e of errors) console.error(`- ${e}`);
  process.exit(1);
}

const payload = {
  _comment:
    'GENERATED by scripts/compile-route-artifacts.mjs — do not hand-edit paths; run npm run compile:routes',
  outputFiles,
  dynamicLandingModules,
};

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, JSON.stringify(payload, null, 2) + '\n', 'utf8');
console.log(
  `Wrote ${outputFiles.length} static entries + ${dynamicLandingModules.length} dynamic module(s) to ${path.relative(root, outPath)}`,
);
